public class datatypes {
    public static void main(String[] agrs){
        int min = Integer.MIN_VALUE;
        int max = Integer.MAX_VALUE;
        System.out.println("Minimum int value : " + min);
        System.out.println("Maximum int value : " + max);

        byte bmin = Byte.MIN_VALUE;
        byte bmax = Byte.MAX_VALUE;
        System.out.println("Byte maximum value: " + bmax);
        System.out.println("Byte minimum value: " + bmin);
        
        Short smin = Short.MIN_VALUE;
        Short smax = Short.MAX_VALUE;
        System.out.println("Short maximum value: " + smax);
        System.out.println("Short minimum value: " + smin);

        long lm = 30000000000L;
        System.out.println("Printing long integer :" + lm);

        float f =3.14F;
        System.out.println(f);

        double d = 2.434321541D;
        System.out.println(d);

        float f1 =5f/3f;
        System.out.println("Float value :"+f1);
        double d1 = 5d/3d;
        System.out.println("Double value :"+d1);

    }

}
